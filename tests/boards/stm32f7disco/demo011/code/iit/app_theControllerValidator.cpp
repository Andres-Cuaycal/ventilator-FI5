struct app::theControllerValidator::Impl
{  
    
    size_t _counter {0};
    Config _config {};
    
    Input _input {0};
    Output _target {0};
    
    Impl() 
    { 
         
    }
    
    bool init(const Config &cfg)
    {
        _config = cfg;
        return true;
    }

    bool get(Input &input, Output &target)
    {
        size_t i = _counter%dataLen;
        _input[0] = _matlab_input[i];
        _target[0] = _matlab_target[i].v0;
        _target[1] = _matlab_target[i].v1;
        _target[2] = _matlab_target[i].v2;
        _target[3] = _matlab_target[i].v3;
        input = _input;
        target = _target;
        
        _counter ++;
        return _counter < dataLen;
    }

    bool check(const Output &candidate, const Output &target, float &ae)
    { 
        ae = 0;        
        if(_counter<=dataLen)
        {
            bool ret = true; 
            for(int i=0; i<candidate.size(); i++)
            {
                if(candidate[i] != target[i])
                {
                    ret = false;
                    ae += std::fabs(candidate[i] - target[i]);
                }   
            }
            return ret;            
        }
        return false;
    }  
                      
};



